A Primeira Forma Normal (1NF) exige que:

1. Cada coluna de uma tabela contenha apenas valores atômicos (indivisíveis).
2. Cada valor em uma coluna seja do mesmo tipo.
3. Cada coluna tenha um nome único.
4. A ordem em que os dados são armazenados não importa.

Todas as tabelas (`Fornecedor`, `Produto`, `Combo`, `Sobremesa`, `Bebida`, `Lanche`, `Funcionario`, `Transacao`, `Transacao_Produto`)
atendem a esses critérios. Cada coluna contém apenas valores atômicos, cada valor em uma coluna é do mesmo tipo, cada coluna tem um nome único,
e a ordem dos dados não importa. Portanto, todas as tabelas estão na Primeira Forma Normal (1NF).